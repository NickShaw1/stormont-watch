export const dynamic = 'force-static'
export const dynamicParams = false

import type { Metadata } from 'next'
import { notFound } from 'next/navigation'
import { ALL_ARCHIVE_MANDATES, mandateById, mandateHasBegun } from '@/lib/constants/mandates'
import SpendingPageBody from '@/app/assembly/stats/spending/SpendingPageBody'

export function generateStaticParams() {
  return ALL_ARCHIVE_MANDATES.map((m) => ({ mandate: m.id }))
}

export async function generateMetadata({ params }: { params: Promise<{ mandate: string }> }): Promise<Metadata> {
  const { mandate: id } = await params
  const m = mandateById(id)
  return {
    title: `Spending - Stats - ${m?.label ?? id} archive`,
    description: `Salaries, office expenses and overall public cost of the Northern Ireland Assembly since ${m?.startLabel ?? id}.`,
  }
}

export default async function ArchiveSpendingPage({ params }: { params: Promise<{ mandate: string }> }) {
  const { mandate: id } = await params
  const mandate = mandateById(id)
  if (!mandate || !mandateHasBegun(mandate)) notFound()
  return <SpendingPageBody mandate={mandate} basePath={`/archive/${id}`} />
}
