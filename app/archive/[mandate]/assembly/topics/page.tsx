export const dynamic = 'force-static'

import type { Metadata } from 'next'
import { notFound } from 'next/navigation'
import { ARCHIVED_MANDATES, mandateById } from '@/lib/constants/mandates'
import TopicsPageBody from '@/app/assembly/topics/TopicsPageBody'

export function generateStaticParams() {
  return ARCHIVED_MANDATES.map((m) => ({ mandate: m.id }))
}

export async function generateMetadata({ params }: { params: Promise<{ mandate: string }> }): Promise<Metadata> {
  const { mandate: id } = await params
  const m = mandateById(id)
  return {
    title: `MLA Topics - ${m?.label ?? id} archive`,
    description: `Debate topics spoken on by MLAs during the ${m?.label ?? id} mandate.`,
  }
}

export default async function ArchiveTopicsPage({ params }: { params: Promise<{ mandate: string }> }) {
  const { mandate: id } = await params
  const mandate = mandateById(id)
  if (!mandate) notFound()
  return <TopicsPageBody mandate={mandate} basePath={`/archive/${id}`} />
}
